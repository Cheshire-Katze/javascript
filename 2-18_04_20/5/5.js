let result;

function sum(a,b) {
    result = a+b;
    return result;
}

function subtract(a,b) {
    result = a-b;
    return result;
}

function multiply(a,b) {
    result = a*b;
    return result;
}

function divide(a,b) {
    result = a/b;
    return result;
}

alert(sum(6,2))
alert(subtract(6,2));
alert(multiply(6,2));
alert(divide(6,2));
